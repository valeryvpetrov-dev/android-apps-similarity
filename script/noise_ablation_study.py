def evaluate_synthetic_pairs(
    app_analyses: Dict[str, AppAnalysis],
    pair_specs: Sequence[Dict[str, str]],
) -> List[Dict[str, Any]]:
    results = []
    for pair_spec in pair_specs:
        analysis_a = app_analyses[pair_spec["app_a"]]
        analysis_b = app_analyses[pair_spec["app_b"]]
        score_without = score_synthetic_tokens(analysis_a.full_graph_tokens, analysis_b.full_graph_tokens)
        score_with = score_synthetic_tokens(analysis_a.filtered_graph_tokens, analysis_b.filtered_graph_tokens)
        results.append(
            build_pair_result(
                pair_id=pair_spec["pair"],
                app_a=analysis_a,
                app_b=analysis_b,
                score_with=score_with,
                score_without=score_without,
            )
        )
    return results


def score_synthetic_tokens(tokens_a: Sequence[str], tokens_b: Sequence[str]) -> float:
    if not tokens_a or not tokens_b:
        return 0.0
    set_a = set(tokens_a)
    set_b = set(tokens_b)
    denominator = max(len(set_a), len(set_b))
    if denominator == 0:
        return 0.0
    return float(len(set_a & set_b) / denominator)


def finalize_report(
    mode: str,
    apk_root: Optional[Path],
    output_path: Optional[Path],
    pair_results: List[Dict[str, Any]],
    app_analyses: Dict[str, AppAnalysis],
    warnings: Sequence[str],
) -> Dict[str, Any]:
    ranking_with = build_ranking(pair_results, "score_with")
    ranking_without = build_ranking(pair_results, "score_without")

    for pair_result in pair_results:
        pair_key = pair_result["pair"]
        pair_result["ranking_with"] = ranking_with[pair_key]
        pair_result["ranking_without"] = ranking_without[pair_key]
        pair_result["ranking_changed"] = ranking_with[pair_key] != ranking_without[pair_key]

    summary = build_summary(pair_results)
    conclusion = build_conclusion(summary)
    human_summary = build_human_summary(
        mode=mode,
        apk_root=apk_root,
        pair_results=pair_results,
        summary=summary,
        conclusion=conclusion,
        warnings=warnings,
    )

    report = {
        "generated_at_utc": utc_timestamp(),
        "mode": mode,
        "apk_dir": str(apk_root) if apk_root is not None else None,
        "pairs": pair_results,
        "summary": summary,
        "conclusion": conclusion,
        "warnings": unique_strings(list(warnings)),
        "apps": build_app_section(app_analyses),
        "human_summary": human_summary,
        "artifacts": {
            "json_report": str(output_path) if output_path is not None else None,
            "text_summary": str(summary_output_path(output_path)) if output_path is not None else None,
        },
    }
    if output_path is not None:
        write_report(report, output_path)
    return report


def build_ranking(pair_results: Sequence[Dict[str, Any]], score_key: str) -> Dict[str, int]:
    ordered = sorted(
        pair_results,
        key=lambda item: (-float(item[score_key]), item["pair"]),
    )
    return {item["pair"]: index + 1 for index, item in enumerate(ordered)}


def build_summary(pair_results: Sequence[Dict[str, Any]]) -> Dict[str, Any]:
    if not pair_results:
        return {
            "pair_count": 0,
            "mean_delta": 0.0,
            "max_delta": 0.0,
            "max_delta_pair": None,
            "min_delta": 0.0,
            "min_delta_pair": None,
            "max_abs_delta": 0.0,
            "max_abs_delta_pair": None,
            "pairs_where_delta_gt_0_05": [],
            "pairs_where_abs_delta_gt_0_05": [],
            "pairs_where_ranking_changed": [],
            "category_impact": [],
        }

    deltas = [float(item["delta"]) for item in pair_results]
    max_pair = max(pair_results, key=lambda item: float(item["delta"]))
    min_pair = min(pair_results, key=lambda item: float(item["delta"]))
    max_abs_pair = max(pair_results, key=lambda item: abs(float(item["delta"])))
    pairs_where_delta_gt = [item["pair"] for item in pair_results if float(item["delta"]) > 0.05]
    pairs_where_abs_delta_gt = [item["pair"] for item in pair_results if abs(float(item["delta"])) > 0.05]
    pairs_where_ranking_changed = [item["pair"] for item in pair_results if item.get("ranking_changed")]

    return {
        "pair_count": len(pair_results),
        "mean_delta": round(float(sum(deltas) / len(deltas)), 6),
        "max_delta": round(float(max_pair["delta"]), 6),
        "max_delta_pair": max_pair["pair"],
        "min_delta": round(float(min_pair["delta"]), 6),
        "min_delta_pair": min_pair["pair"],
        "max_abs_delta": round(abs(float(max_abs_pair["delta"])), 6),
        "max_abs_delta_pair": max_abs_pair["pair"],
        "pairs_where_delta_gt_0_05": pairs_where_delta_gt,
        "pairs_where_abs_delta_gt_0_05": pairs_where_abs_delta_gt,
        "pairs_where_ranking_changed": pairs_where_ranking_changed,
        "category_impact": build_category_impact(pair_results),
    }


def build_category_impact(pair_results: Sequence[Dict[str, Any]]) -> List[Dict[str, Any]]:
    stats = {}
    pair_sets = defaultdict(set)
    for category in SUMMARY_ORDER:
        stats[category] = {
            "noise_elements": 0,
            "graphs_filtered": 0,
            "weighted_abs_delta": 0.0,
            "max_abs_delta_seen": 0.0,
        }

    for pair_result in pair_results:
        abs_delta = abs(float(pair_result["delta"]))
        total_graphs_filtered = max(1, int(pair_result.get("graphs_filtered", 0)))
        for category, values in pair_result["noise_by_category"].items():
            noise_elements = int(values.get("noise_elements", 0))
            graphs_filtered = int(values.get("graphs_filtered", 0))
            if noise_elements <= 0 and graphs_filtered <= 0:
                continue
            stats[category]["noise_elements"] += noise_elements
            stats[category]["graphs_filtered"] += graphs_filtered
            if graphs_filtered > 0:
                stats[category]["weighted_abs_delta"] += abs_delta * (graphs_filtered / total_graphs_filtered)
                stats[category]["max_abs_delta_seen"] = max(stats[category]["max_abs_delta_seen"], abs_delta)
            pair_sets[category].add(pair_result["pair"])

    result = []
    for category in SUMMARY_ORDER:
        if not pair_sets[category]:
            continue
        result.append(
            {
                "category": category,
                "pairs_touched": len(pair_sets[category]),
                "noise_elements": int(stats[category]["noise_elements"]),
                "graphs_filtered": int(stats[category]["graphs_filtered"]),
                "weighted_abs_delta": round(float(stats[category]["weighted_abs_delta"]), 6),
                "max_abs_delta_seen": round(float(stats[category]["max_abs_delta_seen"]), 6),
            }
        )
    result.sort(key=lambda item: (-item["weighted_abs_delta"], -item["graphs_filtered"], -item["noise_elements"], item["category"]))
    return result


def build_conclusion(summary: Dict[str, Any]) -> Dict[str, Any]:
    category_impact = summary.get("category_impact", [])
    if not category_impact:
        return {
            "top_categories": [],
            "inference_method": (
                "Categories are ordered by weighted_abs_delta, which distributes each pair |delta| "
                "proportionally to the number of filtered graphs in that category."
            ),
            "message": "No measurable noise categories were observed in the evaluated pairs.",
        }

    top_categories = [item["category"] for item in category_impact[:3]]
    impactful = [item for item in category_impact if float(item["weighted_abs_delta"]) > 0]
    if impactful:
        message = (
            "Largest measured score shifts are associated with categories {}.".format(", ".join(top_categories))
        )
    else:
        message = (
            "Noise categories were detected, but none produced measurable score changes in this code-only M_static comparison."
        )
    return {
        "top_categories": top_categories,
        "inference_method": (
            "Categories are ordered by weighted_abs_delta, which distributes each pair |delta| "
            "proportionally to the number of filtered graphs in that category."
        ),
        "message": message,
    }


def build_app_section(app_analyses: Dict[str, AppAnalysis]) -> List[Dict[str, Any]]:
    result = []
    for app_id in sorted(app_analyses):
        analysis = app_analyses[app_id]
        full_count = len(analysis.full_graphs) if analysis.source_mode == "real" else len(analysis.full_graph_tokens)
        filtered_count = len(analysis.filtered_graphs) if analysis.source_mode == "real" else len(analysis.filtered_graph_tokens)
        result.append(
            {
                "app_id": analysis.app_id,
                "noise_source": analysis.noise_source,
                "full_graph_count": full_count,
                "filtered_graph_count": filtered_count,
                "noise_elements_by_category": analysis.noise_elements_by_category,
                "graphs_filtered_by_category": analysis.graphs_filtered_by_category,
                "warnings": analysis.warnings,
            }
        )
    return result


def build_human_summary(
    mode: str,
    apk_root: Optional[Path],
    pair_results: Sequence[Dict[str, Any]],
    summary: Dict[str, Any],
    conclusion: Dict[str, Any],
    warnings: Sequence[str],
) -> str:
    lines = []
    lines.append("Noise Ablation Study")
    lines.append("Generated at UTC: {}".format(utc_timestamp()))
    lines.append("Mode: {}".format(mode))
    if apk_root is not None:
        lines.append("APK root: {}".format(apk_root))
    lines.append("")
    lines.append("Pair".ljust(36) + "with".rjust(10) + "without".rjust(12) + "delta".rjust(10) + "noise".rjust(10) + "graphs".rjust(10))
    lines.append("-" * 88)
    for pair_result in pair_results:
        lines.append(
            "{}{}{}{}{}{}".format(
                trim_text(pair_result["pair"], 36).ljust(36),
                "{:.4f}".format(float(pair_result["score_with"])).rjust(10),
                "{:.4f}".format(float(pair_result["score_without"])).rjust(12),
                "{:+.4f}".format(float(pair_result["delta"])).rjust(10),
                str(int(pair_result["noise_elements_filtered"])).rjust(10),
                str(int(pair_result["graphs_filtered"])).rjust(10),
            )
        )

    lines.append("")
    lines.append("Summary")
    lines.append("- mean_delta: {:+.6f}".format(float(summary["mean_delta"])))
    lines.append("- max_delta: {:+.6f} ({})".format(float(summary["max_delta"]), summary["max_delta_pair"]))
    lines.append("- max_abs_delta: {:.6f} ({})".format(float(summary["max_abs_delta"]), summary["max_abs_delta_pair"]))
    lines.append("- pairs_where_delta_gt_0_05: {}".format(", ".join(summary["pairs_where_delta_gt_0_05"]) or "none"))
    lines.append(
        "- pairs_where_ranking_changed: {}".format(", ".join(summary["pairs_where_ranking_changed"]) or "none")
    )

    lines.append("")
    lines.append("Top Noise Categories")
    category_impact = summary.get("category_impact", [])
    if category_impact:
        for category in category_impact[:3]:
            lines.append(
                "- {}: weighted_abs_delta={}, graphs_filtered={}, noise_elements={}".format(
                    category["category"],
                    "{:.6f}".format(float(category["weighted_abs_delta"])),
                    category["graphs_filtered"],
                    category["noise_elements"],
                )
            )
    else:
        lines.append("- none")

    lines.append("")
    lines.append("Conclusion")
    lines.append("- {}".format(conclusion["message"]))

    if warnings:
        lines.append("")
        lines.append("Warnings")
        for warning in unique_strings(list(warnings)):
            lines.append("- {}".format(warning))

    return os.linesep.join(lines)


def trim_text(value: str, limit: int) -> str:
    if len(value) <= limit:
        return value
    if limit <= 1:
        return value[:limit]
    return value[: limit - 1] + "…"


def summary_output_path(output_path: Path) -> Path:
    suffix = output_path.suffix or ".json"
    return output_path.with_suffix(suffix + ".summary.txt")


def write_report(report: Dict[str, Any], output_path: Path) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(json.dumps(report, indent=2, ensure_ascii=False) + os.linesep, encoding="utf-8")
    summary_path = summary_output_path(output_path)
    summary_path.write_text(report["human_summary"] + os.linesep, encoding="utf-8")


def unique_strings(values: Iterable[str]) -> List[str]:
    ordered = []
    seen = set()
    for value in values:
        if not value:
            continue
        if value in seen:
            continue
        seen.add(value)
        ordered.append(value)
    return ordered


@contextmanager
def working_directory(path: Path):
    previous = Path.cwd()
    os.chdir(path)
    try:
        yield
    finally:
        os.chdir(previous)


