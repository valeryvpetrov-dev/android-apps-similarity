def test_extract_noise_profile_fields_no_envelope():
    """_extract_noise_profile_fields returns empty when no envelope present."""
    app_record = _make_app_record("com.example.app")
    warnings, ref = _extract_noise_profile_fields(app_record)

    assert warnings == []
    assert ref is None

    print("PASS: test_extract_noise_profile_fields_no_envelope")


def test_extract_noise_profile_fields_with_envelope():
    """_extract_noise_profile_fields extracts warnings and builds ref correctly."""
    envelope_dict = to_dict(NoiseProfileEnvelope(
        detector_source=DETECTOR_PREFIX_CATALOG_V1,
        status=STATUS_PARTIAL,
        downstream_warnings=["catalog_only", "fallback_used"],
    ))
    app_record = _make_app_record("com.example.app", envelope_dict=envelope_dict)
    warnings, ref = _extract_noise_profile_fields(app_record)

    assert "catalog_only" in warnings
    assert "fallback_used" in warnings
    assert ref is not None
    assert DETECTOR_PREFIX_CATALOG_V1 in ref
    assert STATUS_PARTIAL in ref
    assert SCHEMA_VERSION in ref

    print("PASS: test_extract_noise_profile_fields_with_envelope")


def test_screening_runner_propagates_warnings():
    """build_candidate_list must propagate downstream_warnings into screening_warnings."""
    envelope_dict = to_dict(NoiseProfileEnvelope(
        detector_source=DETECTOR_LIBRARY_VIEW_V2,
        status=STATUS_PARTIAL,
        confidence=CONFIDENCE_MEDIUM,
        noise_reason="library_ambiguous",
        downstream_warnings=["ambiguous_library_attribution", "fallback_used"],
        evidence_refs=["apk_path:/tmp/a.apk"],
    ))

    app_a = _make_app_record("com.example.alpha", envelope_dict=envelope_dict)
    app_b = _make_app_record("com.example.beta")

    # Use a minimal cascade config: features=code, metric=jaccard, threshold=0.0.
    import tempfile
    import json
    config = {
        "stages": {
            "screening": {
                "features": ["code"],
                "metric": "jaccard",
                "threshold": 0.0,
            }
        }
    }
    with tempfile.NamedTemporaryFile(mode="w", suffix=".json", delete=False) as f:
        json.dump(config, f)
        config_path = f.name

    from screening_runner import run_screening

    candidate_list = run_screening(
        cascade_config_path=config_path,
        app_records=[app_a, app_b],
    )

    assert len(candidate_list) >= 1, "Should produce at least one candidate pair"

    # Find the pair where alpha is query_app_id.
    alpha_rows = [c for c in candidate_list if c["query_app_id"] == "com.example.alpha"]
    assert len(alpha_rows) >= 1, "alpha should appear as query app"

    row = alpha_rows[0]
    assert "ambiguous_library_attribution" in row["screening_warnings"], (
        "downstream_warnings must be threaded into screening_warnings"
    )
    assert "fallback_used" in row["screening_warnings"]
    assert row["screening_explanation"] is not None
    assert "noise_profile_ref" in row["screening_explanation"]

    print("PASS: test_screening_runner_propagates_warnings")


