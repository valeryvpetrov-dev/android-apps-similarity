def test_build_noise_profile_library_dominant():
    """build_noise_profile on library-dominant APK => status=success, confidence=high."""
    apk_result = _make_apk_result(library_count=20, app_count=2, unstable_count=0)
    envelope = build_noise_profile(apk_result)

    assert envelope.status == STATUS_SUCCESS
    assert envelope.confidence == CONFIDENCE_HIGH
    assert envelope.noise_reason == "library_detected"
    assert "extraction_unstable" not in envelope.downstream_warnings
    assert "apk_path:/tmp/test.apk" in envelope.evidence_refs
    assert "timestamp:2026-04-16T00:00:00+00:00" in envelope.evidence_refs

    print("PASS: test_build_noise_profile_library_dominant")


def test_build_noise_profile_with_unstable():
    """build_noise_profile with unstable elements => status=success|partial, confidence=medium."""
    apk_result = _make_apk_result(library_count=10, app_count=5, unstable_count=2)
    envelope = build_noise_profile(apk_result)

    # Should still be success (library > 50%) but with medium confidence and warnings.
    assert envelope.status in (STATUS_SUCCESS, STATUS_PARTIAL)
    assert envelope.confidence in (CONFIDENCE_MEDIUM, CONFIDENCE_LOW)
    assert "extraction_unstable" in envelope.downstream_warnings

    print("PASS: test_build_noise_profile_with_unstable")


def test_build_noise_profile_empty_result():
    """build_noise_profile on empty APK result => status=blocked."""
    apk_result = {
        "apk_path": "/tmp/empty.apk",
        "timestamp": "2026-04-16T00:00:00+00:00",
        "elements": [],
        "summary": {},
    }
    envelope = build_noise_profile(apk_result)

    assert envelope.status == STATUS_BLOCKED
    assert envelope.confidence == CONFIDENCE_LOW
    assert "fallback_used" in envelope.downstream_warnings

    print("PASS: test_build_noise_profile_empty_result")


def test_build_noise_profile_partial_library():
    """build_noise_profile with low library ratio => status=partial."""
