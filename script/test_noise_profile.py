def test_to_dict_round_trip():
    """to_dict() must produce a plain dict with all required schema fields."""
    envelope = NoiseProfileEnvelope(
        schema_version=SCHEMA_VERSION,
        detector_source=DETECTOR_LIBRARY_VIEW_V2,
        confidence=CONFIDENCE_HIGH,
        status=STATUS_SUCCESS,
        noise_reason="library_detected",
        downstream_warnings=["ambiguous_library_attribution"],
        evidence_refs=["apk_path:/tmp/test.apk"],
    )
    result = to_dict(envelope)

    assert isinstance(result, dict), "to_dict must return dict"
    assert result["schema_version"] == SCHEMA_VERSION
    assert result["detector_source"] == DETECTOR_LIBRARY_VIEW_V2
    assert result["confidence"] == CONFIDENCE_HIGH
    assert result["status"] == STATUS_SUCCESS
    assert result["noise_reason"] == "library_detected"
    assert result["downstream_warnings"] == ["ambiguous_library_attribution"]
    assert result["evidence_refs"] == ["apk_path:/tmp/test.apk"]

    # Verify it is a copy — mutating result must not affect envelope.
    result["downstream_warnings"].append("extra")
    assert len(envelope.downstream_warnings) == 1, "to_dict must return a copy of lists"

    print("PASS: test_to_dict_round_trip")


def test_default_envelope_fields():
    """Default NoiseProfileEnvelope must have correct schema defaults."""
    envelope = NoiseProfileEnvelope()
    assert envelope.schema_version == SCHEMA_VERSION
    assert envelope.detector_source == DETECTOR_LIBRARY_VIEW_V2
    assert envelope.confidence == CONFIDENCE_HIGH
    assert envelope.status == STATUS_SUCCESS
    assert isinstance(envelope.downstream_warnings, list)
    assert isinstance(envelope.evidence_refs, list)

    print("PASS: test_default_envelope_fields")


