def _make_apk_result(
    library_count: int = 10,
    app_count: int = 5,
    unstable_count: int = 0,
    apk_path: str = "/tmp/test.apk",
) -> dict:
    """Build a minimal mock noise_normalizer output dict."""
    elements = (
        [{"path": "smali/com/google/Foo.smali", "category": "library_like", "reason": "known library prefix com.google"}] * library_count
        + [{"path": "smali/com/example/Bar.smali", "category": "app_specific", "reason": "smali file outside library"}] * app_count
        + [{"path": "smali/broken.smali", "category": "unstable_extraction_risk", "reason": "empty smali file"}] * unstable_count
    )
    summary = {
        "library_like": library_count,
        "app_specific": app_count,
        "unstable_extraction_risk": unstable_count,
        "autogenerated_like": 0,
        "packaging_noise": 0,
    }
    return {
        "apk_path": apk_path,
        "timestamp": "2026-04-16T00:00:00+00:00",
        "elements": elements,
        "summary": summary,
    }


def _make_app_record(app_id: str, envelope_dict: dict | None = None) -> dict:
    """Build a minimal app_record as used by screening_runner."""
    record: dict = {
        "app_id": app_id,
        "layers": {
            "code": {"dex:classes.dex"},
            "component": set(),
            "resource": set(),
            "metadata": {"apk_name:{}".format(app_id)},
            "library": set(),
        },
        "apk_path": None,
    }
    if envelope_dict is not None:
        record["noise_profile_envelope"] = envelope_dict
    return record


