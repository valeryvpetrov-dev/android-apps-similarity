    assert "noise_summary_ref" in artifacts
    assert NOISE_STATUS_UNKNOWN in artifacts["noise_summary_ref"]

    # Original must not be mutated.
    assert "noise_profile_ref" not in pairwise["artifacts"]
    print("PASS: test_propagate_noise_to_pairwise_artifacts")


