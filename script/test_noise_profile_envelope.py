def test_add_noise_context_to_explanation_fields():
    """add_noise_context_to_explanation must add noise_context block."""
    explanation = _make_explanation()
    envelope = NoiseProfileEnvelope(
        noise_status=NOISE_STATUS_NOISY,
        detector_source=DETECTOR_LIBRARY_VIEW_V2,
        confidence=CONFIDENCE_HIGH,
        noise_reason="library_detected",
        downstream_warnings=[],
        status=STATUS_SUCCESS,
    )
    result = add_noise_context_to_explanation(explanation, envelope)

    nc = result["noise_context"]
    assert nc["noise_status"] == NOISE_STATUS_NOISY
    assert nc["detector_source"] == DETECTOR_LIBRARY_VIEW_V2
    assert nc["confidence"] == CONFIDENCE_HIGH
    assert nc["noise_reason"] == "library_detected"
    assert nc["reliability_degraded"] is False
    # No warnings present => key should not exist.
    assert "warnings" not in nc

    # Original must not be mutated.
    assert "noise_context" not in explanation
    print("PASS: test_add_noise_context_to_explanation_fields")


def test_add_noise_context_reliability_degraded():
    """add_noise_context must flag reliability_degraded for blocked/partial status."""
    explanation = _make_explanation()
    envelope = NoiseProfileEnvelope(
        status=STATUS_BLOCKED,
        noise_status=NOISE_STATUS_UNKNOWN,
        downstream_warnings=["fallback_used"],
    )
    result = add_noise_context_to_explanation(explanation, envelope)

    nc = result["noise_context"]
    assert nc["reliability_degraded"] is True
    assert "warnings" in nc
    assert "fallback_used" in nc["warnings"]
    print("PASS: test_add_noise_context_reliability_degraded")


def test_e2e_arbitrate_inject_propagate_chain():
    """E2E: arbitrate -> inject -> propagate -> add_context chain is consistent."""
    # 1. arbitrate
    record = _make_apk_record(library_count=12, app_count=3, use_v2_signal=True)
    envelope = arbitrate_detector(record)

    assert envelope.detector_source == DETECTOR_LIBRARY_VIEW_V2
    assert envelope.noise_status == NOISE_STATUS_NOISY

    # 2. inject into screening pair
    pair = _make_screening_pair()
    pair_with_noise = inject_noise_into_screening_pair(pair, envelope)

    assert pair_with_noise["screening_explanation"]["noise_status"] == NOISE_STATUS_NOISY
    assert "noise_profile_envelope" in pair_with_noise

    # 3. propagate to pairwise
    pairwise = _make_pairwise_result()
    pairwise_with_noise = propagate_noise_to_pairwise(pairwise, envelope)

    assert pairwise_with_noise["run_context"]["noise_status"] == NOISE_STATUS_NOISY
    assert "noise_profile_ref" in pairwise_with_noise["artifacts"]

    # 4. add context to explanation
    explanation = _make_explanation()
    enriched_explanation = add_noise_context_to_explanation(explanation, envelope)

    nc = enriched_explanation["noise_context"]
    assert nc["detector_source"] == DETECTOR_LIBRARY_VIEW_V2
    assert nc["noise_status"] == NOISE_STATUS_NOISY
    assert nc["schema_version"] == SCHEMA_VERSION

    # Consistency: noise_status is the same across all three stages.
    assert (
        pair_with_noise["screening_explanation"]["noise_status"]
        == pairwise_with_noise["run_context"]["noise_status"]
        == nc["noise_status"]
        == NOISE_STATUS_NOISY
    )
    print("PASS: test_e2e_arbitrate_inject_propagate_chain")


