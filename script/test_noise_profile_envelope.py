def test_envelope_defaults():
    """NoiseProfileEnvelope must have correct schema defaults."""
    e = NoiseProfileEnvelope()
    assert e.schema_version == SCHEMA_VERSION
    assert e.detector_source == DETECTOR_LIBRARY_VIEW_V2
    assert e.confidence == CONFIDENCE_HIGH
    assert e.status == STATUS_SUCCESS
    assert e.noise_status == NOISE_STATUS_UNKNOWN
    assert isinstance(e.downstream_warnings, list)
    assert isinstance(e.evidence_refs, list)
    print("PASS: test_envelope_defaults")


def test_to_dict_from_dict_round_trip():
    """to_dict() / from_dict() must produce identical envelope."""
    original = NoiseProfileEnvelope(
        schema_version=SCHEMA_VERSION,
        detector_source=DETECTOR_PREFIX_CATALOG_V1,
        confidence=CONFIDENCE_MEDIUM,
        status=STATUS_PARTIAL,
        noise_reason="library_ambiguous",
        downstream_warnings=["catalog_only", "fallback_used"],
        evidence_refs=["apk_path:/tmp/x.apk", "timestamp:2026-04-16T12:00:00+00:00"],
        noise_status=NOISE_STATUS_UNKNOWN,
    )
    d = to_dict(original)
    restored = from_dict(d)

    assert restored.schema_version == original.schema_version
    assert restored.detector_source == original.detector_source
    assert restored.confidence == original.confidence
    assert restored.status == original.status
    assert restored.noise_reason == original.noise_reason
    assert restored.downstream_warnings == original.downstream_warnings
    assert restored.evidence_refs == original.evidence_refs
    assert restored.noise_status == original.noise_status

    # Verify copies — mutating dict should not affect original.
    d["downstream_warnings"].append("extra")
    assert len(original.downstream_warnings) == 2, "to_dict must return list copies"
    print("PASS: test_to_dict_from_dict_round_trip")


def test_from_dict_missing_keys():
    """from_dict() must tolerate missing keys with safe defaults."""
    e = from_dict({})
    assert e.schema_version == SCHEMA_VERSION
    assert e.noise_status == NOISE_STATUS_UNKNOWN
    assert e.downstream_warnings == []
    print("PASS: test_from_dict_missing_keys")


def test_from_dict_extra_keys():
    """from_dict() must ignore unknown keys without raising."""
    e = from_dict({"unknown_key": "ignored", "another": 42, "status": STATUS_BLOCKED})
    assert e.status == STATUS_BLOCKED
    print("PASS: test_from_dict_extra_keys")


def test_from_dict_type_error():
    """from_dict() must raise TypeError for non-dict input."""
