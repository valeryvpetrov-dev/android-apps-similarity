def _make_apk_record(
    library_count: int = 10,
    app_count: int = 5,
    unstable_count: int = 0,
    apk_path: str = "/tmp/test.apk",
    use_v2_signal: bool = False,
    offline_fingerprint: dict | None = None,
) -> dict:
    """Build a minimal mock apk_record dict."""
    reason_lib = "tpl:com.google known library" if use_v2_signal else "known library prefix com.google"
    elements = (
        [{"path": "smali/com/google/Foo.smali", "category": "library_like", "reason": reason_lib}] * library_count
        + [{"path": "smali/com/example/Bar.smali", "category": "app_specific", "reason": "smali file outside library"}] * app_count
        + [{"path": "smali/broken.smali", "category": "unstable_extraction_risk", "reason": "empty smali file"}] * unstable_count
    )
    summary = {
        "library_like": library_count,
        "app_specific": app_count,
        "unstable_extraction_risk": unstable_count,
        "autogenerated_like": 0,
        "packaging_noise": 0,
    }
    record: dict = {
        "apk_path": apk_path,
        "timestamp": "2026-04-16T12:00:00+00:00",
        "elements": elements,
        "summary": summary,
    }
    if offline_fingerprint is not None:
        record["offline_fingerprint"] = offline_fingerprint
    return record


def _make_screening_pair(
    app_a: str = "com.example.alpha",
    app_b: str = "com.example.beta",
    existing_warnings: list | None = None,
) -> dict:
    return {
        "app_a": app_a,
        "app_b": app_b,
        "query_app_id": app_a,
        "candidate_app_id": app_b,
        "retrieval_score": 0.75,
        "screening_warnings": list(existing_warnings or []),
        "screening_explanation": None,
    }


def _make_pairwise_result(pair_id: str = "pair-001") -> dict:
    return {
        "pair_id": pair_id,
        "analysis_status": "complete",
        "artifacts": {
            "artifacts_path": "pairwise://{}".format(pair_id),
        },
        "run_context": {
            "dataset_id": "ds-001",
        },
        "scores": {"jaccard": 0.8},
    }


def _make_explanation(analysis_status: str = "complete") -> dict:
    return {
        "explanation_status": "available",
        "analysis_status": analysis_status,
        "score_interpretation": "high similarity",
    }


