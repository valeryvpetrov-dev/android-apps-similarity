def test_inject_noise_into_screening_pair_warnings_merge():
    """inject_noise_into_screening_pair must merge warnings without duplicates."""
    pair = _make_screening_pair(existing_warnings=["pre_existing"])
    envelope = NoiseProfileEnvelope(
        downstream_warnings=["pre_existing", "new_warning"],
        noise_status=NOISE_STATUS_NOISY,
        status=STATUS_SUCCESS,
    )
    result = inject_noise_into_screening_pair(pair, envelope)

    warnings = result["screening_warnings"]
    assert "pre_existing" in warnings
    assert "new_warning" in warnings
    assert warnings.count("pre_existing") == 1, "No duplicates"

    # Original must not be mutated.
    assert pair["screening_warnings"] == ["pre_existing"]
    print("PASS: test_inject_noise_into_screening_pair_warnings_merge")


def test_inject_noise_into_screening_pair_explanation_fields():
    """inject_noise_into_screening_pair must set noise fields in screening_explanation."""
    pair = _make_screening_pair()
    envelope = NoiseProfileEnvelope(
        detector_source=DETECTOR_LIBRARY_VIEW_V2,
        status=STATUS_SUCCESS,
        noise_status=NOISE_STATUS_NOISY,
    )
    result = inject_noise_into_screening_pair(pair, envelope)

    explanation = result["screening_explanation"]
    assert explanation is not None
    assert "noise_profile_ref" in explanation
    assert explanation["noise_status"] == NOISE_STATUS_NOISY
    assert "noise_profile_envelope" in result
    assert result["noise_profile_envelope"]["detector_source"] == DETECTOR_LIBRARY_VIEW_V2
    print("PASS: test_inject_noise_into_screening_pair_explanation_fields")


def test_inject_merges_existing_explanation():
    """inject_noise_into_screening_pair must preserve existing explanation keys."""
    pair = _make_screening_pair()
    pair["screening_explanation"] = {"existing_key": "existing_value"}
    envelope = NoiseProfileEnvelope(noise_status=NOISE_STATUS_CLEAN)
    result = inject_noise_into_screening_pair(pair, envelope)

    explanation = result["screening_explanation"]
    assert explanation["existing_key"] == "existing_value"
    assert "noise_profile_ref" in explanation
